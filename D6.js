/* ESERCIZIO 9 (forEach)
  Scrivi una funzione per trovare il film più vecchio nell'array fornito.
*/

const findOldest = (movies) => {
  let oldestYear = movies[0].Year;
  let oldestMovie = movies[0].Title;
  movies.forEach((movie) => {
    const year = movie.Year;
    if (year < oldestYear) {
      oldestYear = year;
      oldestMovie = movie.Title;
    }
  });
  return oldestMovie;
};

findOldest(movies);

console.log(findOldest(movies));

